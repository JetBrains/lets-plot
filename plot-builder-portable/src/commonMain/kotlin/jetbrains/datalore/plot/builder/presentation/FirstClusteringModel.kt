        6 to 23.727272727272727
    )

    private val BASIC_FAMILY_NAME = "Lucida Grande"

    private val FAMILY_COEFFICIENT = mapOf(
        "Arial" to 0.0284362571276161,
        "Calibri" to -0.8162313054057978,
        "Garamond" to -1.293468048837939,
        "Geneva" to 0.0284362571276161,
        "Georgia" to 0.00694993273888036,
        "Helvetica" to 0.0284362571276161,
        "Lucida Grande" to 0.0284362571276161,
        "Rockwell" to 0.3577677684513799,
        "Times New Roman" to -1.260095811304983,
        "Verdana" to 1.244869565729623
    )

    private val DEFAULT_FAMILY_COEFFICIENT: Double = FAMILY_COEFFICIENT[BASIC_FAMILY_NAME]!!

    private const val SIZE_COEFFICIENT = 0.9799416549509185

    private fun getFamilyCoefficient(font: Font): Double {
        val fontFamily = font.family.toString()
        return if (fontFamily in FAMILY_COEFFICIENT.keys) FAMILY_COEFFICIENT[fontFamily]!! else DEFAULT_FAMILY_COEFFICIENT
    }

    private fun getFaceCoefficient(font: Font): Double {
        return when {
            font.isBold && font.isItalic -> 0.9285468850863421
            font.isBold && !font.isItalic -> 0.7021586745974023
            !font.isBold && font.isItalic -> 0.18965364765291592
            else -> 0.0113347855792421
        }
    }

    private const val BASIC_FONT_SIZE = 14

    private fun getSizeCoefficient(font: Font): Double {
        return SIZE_COEFFICIENT * font.size / BASIC_FONT_SIZE
    }

    private fun isBaseFont(font: Font): Boolean {
        return font.family.toString() == BASIC_FAMILY_NAME && font.size == BASIC_FONT_SIZE
    }

    private fun sumClusters(text: String): Double {
        return text.map {
            val cluster = CLUSTERS[it]
            if (!CLUSTER_WIDTH.containsKey(cluster)) println("No width for cluster $cluster; symbol: '$it'")
            CLUSTER_WIDTH[cluster] ?: DEFAULT_WIDTH
        }.sum()
    }

    fun textDimension(
        text: String,
        font: Font,
        sizeRatio: Double,
        boldRatio: Double,
        italicRatio: Double,
        nonBaseFontRatio: Double,
        nonBaseFontAdditiveError: Double,
    ): DoubleVector {
        if (text.isEmpty()) {
            return DoubleVector.ZERO
        }

        val width = sumClusters(text).let {
            var w = it + text.length * getFamilyCoefficient(font)
            w += text.length * getFaceCoefficient(font)
            if (font.isBold) w *= boldRatio
            if (font.isItalic) w *= italicRatio
            w *= getSizeCoefficient(font) * sizeRatio
            if (!isBaseFont(font)) w = w * nonBaseFontRatio + text.length * nonBaseFontAdditiveError
            w
        }

        return DoubleVector(width, font.size.toDouble())
    }
